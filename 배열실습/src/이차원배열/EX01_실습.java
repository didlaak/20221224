package 이차원배열;

public class EX01_실습 {

	public static void main(String[] args) {

		
		//이차원배열 선언 및 새성
		
		
		int [][] array = new int [4][5];
		System.out.println(array[3][4]);
		
		array[0][0] = 10;
		System.out.println(array[0][0]);
		
		
		
		
	}

}
// 이차원 배열
// -> 배열안에 배열이 들어있는 형태
// -> 행과 열로 이루어져 있음

// 선언 및 생성
// 자료형 [][] 레퍼런스변수 = new 자료형[행][열];

// 배열의 크기(.length)
// array.length -> 행의 갯수(바깥배열의 크기)
// array[행].length -> 열의 갯수(안쪽배열의 크기)












